package prerequisites;

public class Programme7 {

	public static void main(String[] args) {
		
//       1. for-loop - It executes how many times we want to print the print
		for(int i=1; i<=5; i++) {
		System.out.println("Scahin Tendulkar");
		}
//      2. while-loop - it executes until statement is true or we don't know the condition.
		int i=1;
		while(i<=5) {
			System.out.println("Sara Tendulkar");
			i++;
		}
//     3.do-while loop - It executes atleast 1 times if given condition is not true.
		
		int j=1;
		do {
			System.out.println("Shubman Gill");
			j++;
		}while(j<=5);
	}

}
